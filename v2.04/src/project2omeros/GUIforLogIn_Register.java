    private void log_RegBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_log_RegBActionPerformed
        Users temp = new Users();
        if(userNameT.getText().contains(" ")||passwordT.getText().contains(" ")){ //den theloume space sta fields giati me space xorizonte oi metablites sto userMap.
            JOptionPane.showMessageDialog(null, "Please don't put space in the fields.", 
                    "Error message", JOptionPane.PLAIN_MESSAGE);
        }else{
            if(log_RegB.getText().equals("Log In")){ //efoson to log_RegB exei text Log In o xristis thelei na kanei log in
                dispose();
                temp.logIn(userNameT.getText(), passwordT.getText());
            }else if(AdmorUsRadio().equals("A")){ //ama to log_RegB den exei text Log In o xristis thelei na kanei register os admin
                if(AdmCheck()){ //elenxei ama yparxei admin account
                    dispose();
                    temp.registerNewUser(userNameT.getText(), passwordT.getText(),passwordT1.getText(), AdmorUsRadio());
                }else{ //den uparxei admin account
                    JOptionPane.showMessageDialog(null, "There is no admins account with\nthat username or password.", 
                            "Security message", JOptionPane.PLAIN_MESSAGE);
                }
            }else{ //thelei na kanei register os user (toAdmorUsRadio exei epilegei to option gia register os user)
                dispose();
                temp.registerNewUser(userNameT.getText(), passwordT.getText(),passwordT1.getText(), AdmorUsRadio());
            }
        }
    }//GEN-LAST:event_log_RegBActionPerformed

