    private void showHallBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showHallBActionPerformed
        editSaveB.setEnabled(true);
        bookSeatsB.setEnabled(true);
        HallSeats temp = new HallSeats();
        List<String> info;
        if(eventNameT.getText().equals("Cinema halls")){
            if(hallsL.getSelectedItem().equals("No halls")){
                addAsNewHallB.doClick();
                hallNameTshow.setText("There is no hall for that movie");
                displayDressingTshow.setText("-");
                soundBalconiesTshow.setText("-");
                horizonticallyTshow.setValue(99);
                verticallyTshow.setValue(99);
            }else{
                deleteHallB.setEnabled(true);
                String nameOfHall = hallsL.getSelectedItem().toString();
                info = temp.showHall(nameOfHall, true);
                hallNameTshow.setText(nameOfHall);
                displayDressingTshow.setText(info.get(1));
                soundBalconiesTshow.setText(info.get(2));
                horizonticallyTshow.setValue(Integer.parseInt(info.get(3)));
                verticallyTshow.setValue(Integer.parseInt(info.get(4)));
            }
        }else if(eventNameT.getText().equals("theater halls")){
            if(hallsL.getSelectedItem().equals("No halls")){
                addAsNewHallB.doClick();
                hallNameTshow.setText("There is no hall for that performance");
                balconiesTshow.setValue(99);
                dressingRoomsTshow.setValue(99);
                horizonticallyTshow.setValue(99);
                verticallyTshow.setValue(99);
            }else{
                deleteHallB.setEnabled(true);
                String nameOfHall = hallsL.getSelectedItem().toString();
                info = temp.showHall(nameOfHall,false);
                hallNameTshow.setText(nameOfHall);
                balconiesTshow.setValue(Integer.parseInt(info.get(1)));
                dressingRoomsTshow.setValue(Integer.parseInt(info.get(2)));
                horizonticallyTshow.setValue(Integer.parseInt(info.get(3)));
                verticallyTshow.setValue(Integer.parseInt(info.get(4)));
            }
        }
    }//GEN-LAST:event_showHallBActionPerformed

    private void editSaveBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSaveBActionPerformed
        if(editSaveB.getText().equals("Save hall")){
            if(true){
                //ftiaxnei ta koumpia sto interface
                List<String> info = new ArrayList<String>();
                String hallNameChecker = hallNameTshow.getText();
                if(addAsNewHallB.isSelected()){ // elegxei ama o xristeis thelei na kanei add to event
                    addAsNewHallB.doClick(); //3etsekarei
                    if(hallsL.getSelectedItem().equals("No halls")){
                        if(eventNameT.getText().equals("Cinema halls")){
                            hallsL.removeAllItems(); //Ama thelei na kanei kai to jcompobox den exei teneies tote bgazei to boithitiko event
                        }else{
                            int i=0;
                            while(i<hallsL.getItemCount()){
                                if(hallNameChecker.equals(hallsL.getSelectedItem())){
                                    hallNameChecker= hallNameTshow.getText()+"Copy";
                                    i=-1;
                                }
                                i++;
                            }
                        }
                    }else{
                        if(eventNameT.getText().equals("Cinema halls")){
                            hallsL.removeAllItems();
                        }else{                 
                            int i=0;
                            while(i<hallsL.getItemCount()){
                                if(hallNameChecker.equals(hallsL.getSelectedItem())){
                                    hallNameChecker= hallNameTshow.getText()+"Copy";
                                    i=-1;
                                }
                                i++;
                            }
                        }           
                    }
                }
                if(eventNameT.getText().equals("Cinema halls")){
                    info.add("C");
                    info.add(displayDressingTshow.getText());
                    info.add(soundBalconiesTshow.getText());
                }else{
                    info.add("T");
                    info.add(balconiesTshow.getValue().toString());
                    info.add(dressingRoomsTshow.getValue().toString());
                }
                info.add(horizonticallyTshow.getValue().toString());
                info.add(verticallyTshow.getValue().toString());
                HallSeats temp = new HallSeats();
                temp.editHalls(hallNameChecker, info, eventNameT.getText().equals("Cinema halls"));
//                dispose();
                GUIforHalls temp1 = new GUIforHalls(eventNameT.getText().equals("Cinema halls"));
                temp1.setLocationRelativeTo(null);
                temp1.setVisible(true);
            }else{
                if(hallNameTshow.getText().equals("No halls")){
                    JOptionPane.showMessageDialog(null, "Please put a real name for the hall.",
                            "Error message", JOptionPane.PLAIN_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Please dont use this character sequance "
                            + "\">~~Dnt@Dlt~~<\".", "Error message", JOptionPane.PLAIN_MESSAGE);
                }
            }
        }else if(editSaveB.getText().equals("Edit hall")){ //o xristis patise to koumpi gia na kanei edit to event
            deleteHallB.setEnabled(false);
            hallsL.setEnabled(false);
            editSaveB.setText("Save hall");
            horizonticallyTshow.setEnabled(true);
            verticallyTshow.setEnabled(true);
            balconiesTshow.setEnabled(true);
            dressingRoomsTshow.setEnabled(true);
            addAsNewHallB.setEnabled(true);
            cancel2B.setEnabled(true);
            cancel2B.setVisible(true);
            displayDressingTshow.setEditable(true);
            soundBalconiesTshow.setEditable(true);
            addAsNewHallB.setEnabled(true);
            addAsNewHallBActionPerformed(evt);
            showHallB.setEnabled(false);
            bookSeatsB.setEnabled(false);
            sheatsP.removeAll();
            revalidate();
            repaint();
        }
    }//GEN-LAST:event_editSaveBActionPerformed

