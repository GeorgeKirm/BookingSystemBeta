    private String AdmorUsRadio(){
        if (AdmRadio.isSelected()){ //admRadio exei epilegei
            return "A"; //o xristis thelei na kanei register os admin
        }else{ //UsRadio enabled
            return "U"; //o xristis thelei na kanei register os aplos user
        }        
    }
    
    
    private boolean AdmCheck(){ //ellegxei ama uparxei account tou admin pou edose permission ston neo xristi na kanei register os admin
        if(AdmorUsRadio().equals("A")){
            Admins admintemp = new Admins();
            return admintemp.adminChecker(adusername.getText(), adpassword.getText());
        }else{
            return false;
        }
    }
    
