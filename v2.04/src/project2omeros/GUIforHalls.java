    private void deleteHallBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteHallBActionPerformed
        int n = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this event?", 
                "Security Question", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            HallSeats temp = new HallSeats();
            String nameOfEvent;
            int numberI = hallsL.getSelectedIndex();
            nameOfEvent = hallsL.getSelectedItem().toString();
            if(eventNameT.getText().equals("Cinema halls")){
                temp.deleteHall(nameOfEvent, true);
            }else{
                temp.deleteHall(nameOfEvent, false);
            }
            hallsL.removeItemAt(numberI);
            if(hallsL.getItemCount()==0){
                hallsL.addItem("No halls");
            }
        } else if (n == JOptionPane.NO_OPTION) {

        }
    }//GEN-LAST:event_deleteHallBActionPerformed

